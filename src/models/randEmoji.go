func RandEmojis(size int) (string, error) {
	b := make([]byte, size)

	_, err := rand.Read(b)
	if err != nil {
		return "", err
	}

	var s []string

	count := 0

	for i := 0; i < size; i++ {
		s = append(s, emjMap[b[count]])
		count++
	}

	return strings.Join(s, ""), err
}

